# Basic for loop with range(stop)
for i in range(10):
    print("The value of i is currently", i)

# For loop with range(start, stop)
for i in range(2, 8):
    print("The value of i is currently", i)

# For loop with range(start, stop, step)
for i in range(2, 8, 3):
    print("The value of i is currently", i)